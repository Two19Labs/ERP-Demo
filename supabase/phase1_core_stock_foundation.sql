create or replace view public.user_access_view
with (security_invoker = true)
as
select
  u.id,
  u.full_name,
  u.phone,
  u.is_active,
  r.code as role_code,
  r.label as role_label
from public.users u
join public.roles r on r.id = u.role_id;

create or replace view public.stock_item_balances
with (security_invoker = true)
as
select
  si.id as stock_item_id,
  si.name,
  si.category,
  si.default_unit,
  si.low_stock_threshold,
  si.is_active,
  coalesce(
    sum(
      case sm.movement_type
        when 'opening_stock' then sm.quantity
        when 'purchase_added' then sm.quantity
        when 'usage' then -sm.quantity
        when 'wastage' then -sm.quantity
        when 'return_to_vendor' then -sm.quantity
        when 'correction' then sm.quantity
        else 0
      end
    ),
    0
  ) as current_quantity,
  coalesce(
    sum(
      case sm.movement_type
        when 'opening_stock' then sm.quantity
        when 'purchase_added' then sm.quantity
        when 'usage' then -sm.quantity
        when 'wastage' then -sm.quantity
        when 'return_to_vendor' then -sm.quantity
        when 'correction' then sm.quantity
        else 0
      end
    ),
    0
  ) <= si.low_stock_threshold as is_low_stock
from public.stock_items si
left join public.stock_movements sm on sm.stock_item_id = si.id
group by si.id;

