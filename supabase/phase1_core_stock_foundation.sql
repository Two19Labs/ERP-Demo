create table if not exists public.roles (
  id bigint generated by default as identity primary key,
  code text not null unique,
  label text not null unique,
  created_at timestamptz not null default timezone('utc', now()),
  constraint roles_code_check check (code in ('owner', 'staff'))
);

create table if not exists public.users (
  id uuid primary key references auth.users (id) on delete cascade,
  role_id bigint not null references public.roles (id) on delete restrict,
  full_name text,
  phone text,
  is_active boolean not null default true,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.stock_items (
  id uuid primary key default gen_random_uuid(),
  name text not null unique,
  category text not null,
  default_unit text not null,
  low_stock_threshold numeric(12, 3) not null default 0,
  is_active boolean not null default true,
  notes text,
  created_by uuid references auth.users (id) on delete set null default auth.uid(),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint stock_items_low_stock_threshold_check check (low_stock_threshold >= 0)
);

create table if not exists public.vendors (
  id uuid primary key default gen_random_uuid(),
  name text not null unique,
  contact_name text,
  phone text,
  category_supplied text,
  notes text,
  is_active boolean not null default true,
  created_by uuid references auth.users (id) on delete set null default auth.uid(),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.purchase_bills (
  id uuid primary key default gen_random_uuid(),
  vendor_id uuid references public.vendors (id) on delete restrict,
  bill_date date not null default current_date,
  bill_number text,
  source text not null default 'manual',
  original_text text,
  file_url text,
  subtotal numeric(12, 2) not null default 0,
  extra_charges numeric(12, 2) not null default 0,
  total numeric(12, 2) not null default 0,
  status text not null default 'draft',
  created_by uuid references auth.users (id) on delete set null default auth.uid(),
  approved_by uuid references auth.users (id) on delete set null,
  approved_at timestamptz,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint purchase_bills_status_check check (status in ('draft', 'pending_review', 'approved', 'rejected')),
  constraint purchase_bills_source_check check (source in ('manual', 'whatsapp_paste', 'upload')),
  constraint purchase_bills_money_check check (subtotal >= 0 and extra_charges >= 0 and total >= 0),
  constraint purchase_bills_approval_check check (
    (status = 'approved' and approved_by is not null and approved_at is not null)
    or (status <> 'approved')
  )
);

create table if not exists public.purchase_bill_items (
  id uuid primary key default gen_random_uuid(),
  purchase_bill_id uuid not null references public.purchase_bills (id) on delete cascade,
  stock_item_id uuid references public.stock_items (id) on delete restrict,
  raw_item_name text not null,
  quantity numeric(12, 3) not null,
  unit text not null,
  unit_price numeric(12, 2) not null,
  line_total numeric(12, 2) not null,
  confidence_score numeric(5, 2),
  match_status text not null default 'manual_match',
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint purchase_bill_items_quantity_check check (quantity > 0),
  constraint purchase_bill_items_money_check check (unit_price >= 0 and line_total >= 0),
  constraint purchase_bill_items_confidence_score_check check (confidence_score is null or confidence_score between 0 and 100),
  constraint purchase_bill_items_match_status_check check (match_status in ('matched', 'manual_match', 'unknown', 'needs_review'))
);

create table if not exists public.stock_movements (
  id uuid primary key default gen_random_uuid(),
  stock_item_id uuid not null references public.stock_items (id) on delete restrict,
  movement_type text not null,
  quantity numeric(12, 3) not null,
  unit text not null,
  source_bill_item_id uuid references public.purchase_bill_items (id) on delete restrict,
  notes text,
  created_by uuid references auth.users (id) on delete set null default auth.uid(),
  created_at timestamptz not null default timezone('utc', now()),
  constraint stock_movements_quantity_check check (quantity > 0),
  constraint stock_movements_type_check check (
    movement_type in ('opening_stock', 'purchase_added', 'usage', 'wastage', 'return_to_vendor', 'correction')
  ),
  constraint stock_movements_purchase_source_check check (
    movement_type <> 'purchase_added' or source_bill_item_id is not null
  )
);

