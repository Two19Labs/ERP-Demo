CREATE OR REPLACE VIEW public.user_access_view AS
  SELECT u.id, u.full_name, u.phone, u.is_active,
         r.code AS role_code, r.label AS role_label
  FROM public.users u
  JOIN public.roles r ON r.id = u.role_id;

CREATE OR REPLACE VIEW public.stock_item_balances AS
  SELECT si.id AS stock_item_id, si.name, si.category, si.default_unit,
         si.low_stock_threshold, si.is_active,
         COALESCE(sum(
           CASE sm.movement_type
             WHEN 'opening_stock'    THEN sm.quantity
             WHEN 'purchase_added'   THEN sm.quantity
             WHEN 'usage'            THEN -sm.quantity
             WHEN 'wastage'          THEN -sm.quantity
             WHEN 'return_to_vendor' THEN -sm.quantity
             WHEN 'correction'       THEN sm.quantity
             ELSE 0::numeric
           END), 0::numeric) AS current_quantity,
         (COALESCE(sum(
           CASE sm.movement_type
             WHEN 'opening_stock'    THEN sm.quantity
             WHEN 'purchase_added'   THEN sm.quantity
             WHEN 'usage'            THEN -sm.quantity
             WHEN 'wastage'          THEN -sm.quantity
             WHEN 'return_to_vendor' THEN -sm.quantity
             WHEN 'correction'       THEN sm.quantity
             ELSE 0::numeric
           END), 0::numeric) <= si.low_stock_threshold) AS is_low_stock
  FROM public.stock_items si
  LEFT JOIN public.stock_movements sm ON sm.stock_item_id = si.id
  GROUP BY si.id;


