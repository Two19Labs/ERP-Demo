CREATE OR REPLACE FUNCTION private.current_user_role()
RETURNS text
LANGUAGE sql SECURITY DEFINER SET search_path = 'public','private'
AS $$
  select r.code
  from public.users u
  join public.roles r on r.id = u.role_id
  where u.id = (select auth.uid())
    and u.is_active
  limit 1
$$;

CREATE OR REPLACE FUNCTION private.is_owner()
RETURNS boolean
LANGUAGE sql SECURITY DEFINER SET search_path = 'public','private'
AS $$
  select coalesce(private.current_user_role() = 'owner', false)
$$;

CREATE OR REPLACE FUNCTION private.is_active_app_user()
RETURNS boolean
LANGUAGE sql SECURITY DEFINER SET search_path = 'public','private'
AS $$
  select private.current_user_role() in ('owner','staff')
$$;

CREATE OR REPLACE FUNCTION private.can_access_bill(target_bill_id uuid)
RETURNS boolean
LANGUAGE sql SECURITY DEFINER SET search_path = 'public','private'
AS $$
  select exists (
    select 1 from public.purchase_bills b
    where b.id = target_bill_id
      and (private.is_owner() or b.created_by = (select auth.uid()))
  )
$$;

CREATE OR REPLACE FUNCTION private.can_manage_bill_lines(target_bill_id uuid)
RETURNS boolean
LANGUAGE sql SECURITY DEFINER SET search_path = 'public','private'
AS $$
  select exists (
    select 1 from public.purchase_bills b
    where b.id = target_bill_id
      and (
        private.is_owner()
        or (b.created_by = (select auth.uid())
            and b.status in ('draft','pending_review'))
      )
  )
$$;


