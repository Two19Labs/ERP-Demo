alter table public.users enable row level security;

drop policy if exists "authenticated users can read roles" on public.roles;
create policy "authenticated users can read roles"
on public.roles
for select
to authenticated
using ((select auth.uid()) is not null);

drop policy if exists "hq can read all outlets" on public.outlets;
create policy "hq can read all outlets"
on public.outlets
for select
to authenticated
using (
  private.is_hq()
);

drop policy if exists "managers can read their outlet" on public.outlets;
create policy "managers can read their outlet"
on public.outlets
for select
to authenticated
using (
  id = private.current_outlet_id()
);

drop policy if exists "users can read their own profile" on public.users;
create policy "users can read their own profile"
on public.users
for select
to authenticated
using (
  (select auth.uid()) is not null
  and id = (select auth.uid())
);

drop policy if exists "hq can read all users" on public.users;
create policy "hq can read all users"
on public.users
for select
to authenticated
using (
  private.is_hq()
);

drop policy if exists "hq can manage users" on public.users;
create policy "hq can manage users"
on public.users
for all
to authenticated
using (
  private.is_hq()
)
with check (
  private.is_hq()
);

comment on table public.users is 'App user profile linked 1:1 with auth.users.';

create or replace view public.user_access_view
with (security_invoker = true)
as
select
  u.id,
  u.full_name,
  u.is_active,
  r.code as role_code,
  r.label as role_label,
  o.id as outlet_id,
  o.code as outlet_code,
  o.name as outlet_name,
  o.city,
  o.state_region
from public.users u
join public.roles r on r.id = u.role_id
left join public.outlets o on o.id = u.outlet_id;

grant select on public.outlets to authenticated;
grant select on public.roles to authenticated;
grant select, insert, update on public.users to authenticated;
grant select on public.user_access_view to authenticated;
grant all on public.outlets to service_role;
grant all on public.roles to service_role;
grant all on public.users to service_role;
grant select on public.user_access_view to service_role;

