create extension if not exists pgcrypto;
create schema if not exists private;

create table if not exists public.outlets (
  id uuid primary key default gen_random_uuid(),
  code text not null unique,
  name text not null unique,
  city text not null,
  state_region text not null,
  is_active boolean not null default true,
  created_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.roles (
  id bigint generated by default as identity primary key,
  code text not null unique,
  label text not null unique,
  created_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.users (
  id uuid primary key references auth.users (id) on delete cascade,
  role_id bigint not null references public.roles (id),
  outlet_id uuid references public.outlets (id),
  full_name text,
  is_active boolean not null default true,
  created_at timestamptz not null default timezone('utc', now())
);

create index if not exists users_role_id_idx on public.users (role_id);
create index if not exists users_outlet_id_idx on public.users (outlet_id);

create or replace function private.is_hq()
returns boolean
language plpgsql
security definer
set search_path = public, private
as $$
begin
  return exists (
    select 1
    from public.users u
    join public.roles r on r.id = u.role_id
    where u.id = (select auth.uid())
      and u.is_active
      and r.code = 'hq'
  );
end;
$$;

create or replace function private.current_outlet_id()
returns uuid
language sql
security definer
set search_path = public, private
as $$
  select u.outlet_id
  from public.users u
  where u.id = (select auth.uid())
    and u.is_active
  limit 1
$$;

