create or replace function public.enforce_user_outlet_rule()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  role_code text;
begin
  select r.code into role_code
  from public.roles r
  where r.id = new.role_id;

  if role_code is null then
    raise exception 'Role % does not exist.', new.role_id;
  end if;

  if role_code = 'manager' and new.outlet_id is null then
    raise exception 'Managers must be assigned to an outlet.';
  end if;

  if role_code = 'hq' and new.outlet_id is not null then
    raise exception 'HQ users must not be pinned to a single outlet.';
  end if;

  return new;
end;
$$;

drop trigger if exists users_enforce_outlet_rule on public.users;
create trigger users_enforce_outlet_rule
before insert or update on public.users
for each row
execute function public.enforce_user_outlet_rule();

alter table public.outlets enable row level security;
